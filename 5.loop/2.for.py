
for letter in 'Python':
    print('Current letter: ', letter)

fruits = ['banana', 'apple',  'mango']

for fruit in fruits:
    print('Current fruit: ', fruit)

length = len(fruits)
print('The count of fruits: ', length)

range = range(length)
print('The fruits range: ', range)

for index in range:
    print('The current fruit: ', fruits[index])
